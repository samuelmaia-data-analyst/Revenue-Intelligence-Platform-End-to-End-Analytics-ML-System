def translate(lang: str, key: str, **kwargs: object) -> str:
    text = I18N[lang][key]
    return text.format(**kwargs) if kwargs else text


def normalize_lang(option: str) -> str:
    if "PT-BR" in option:
        return "pt-br"
    if "PT-PT" in option:
        return "pt-pt"
    return "en"
